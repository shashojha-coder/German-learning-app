import json, os, random, openpyxl

random.seed(42)

# ============================================================
# 1) EXTRACT UNIQUE NOUNS FROM EXCEL
# ============================================================
wb = openpyxl.load_workbook("German Noun.xlsx")
ws = wb["Sheet2"]

excel_nouns = []
seen_de = set()
for row in ws.iter_rows(min_row=2, values_only=True):
    num, eng, de_sing, de_plural, notes = row
    if eng and de_sing:
        de_s = str(de_sing).strip()
        if de_s not in seen_de:
            seen_de.add(de_s)
            excel_nouns.append({
                "de": de_s,
                "en": str(eng).strip().lower(),
                "plural": str(de_plural).strip() if de_plural and str(de_plural).strip() != "-" else None,
            })

print(f"Extracted {len(excel_nouns)} unique nouns from Excel")

# ============================================================
# 2) HELPERS
# ============================================================
def mkw(de, en, example):
    return {"de": de, "en": en, "example": example}

def make_example(de_word, en_word):
    """Generate a simple German example sentence for a noun."""
    if de_word.startswith("Der "):
        noun = de_word[4:]
        t = [f"Der {noun} ist groß.", f"Der {noun} ist wichtig.", f"Ich sehe den {noun}.", f"Der {noun} ist hier."]
    elif de_word.startswith("Die "):
        noun = de_word[4:]
        t = [f"Die {noun} ist schön.", f"Die {noun} ist wichtig.", f"Ich sehe die {noun}.", f"Die {noun} ist hier."]
    elif de_word.startswith("Das "):
        noun = de_word[4:]
        t = [f"Das {noun} ist gut.", f"Das {noun} ist wichtig.", f"Ich sehe das {noun}.", f"Das {noun} ist hier."]
    else:
        t = [f"{de_word} ist wichtig.", f"Ich brauche {de_word}."]
    return random.choice(t)

